function setup(){
    createCanvas(400,400);
}

function draw(){
    background(1);
    fill(255);
    noStroke();
    ellipse(200,200,40,40);

    fill(1)
    ellipse(200,200,30,30);

    fill(255)
    ellipse(200,200,20,20);

    fill(255)
    ellipse(0,400,300,300)
    ellipse(0,0,300,300)
    ellipse(400,0,300,300)
    ellipse(400,400,300,300)

    fill(255)
    ellipse(200,0,150,150)
    ellipse(400,200,150,150)
    ellipse(200,400,150,150)
    ellipse(0,200,150,150)
}